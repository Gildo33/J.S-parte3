/* Funcions / funções

   Funcions padrão -> Funcions() {}
   arrow funcions -> () =>{}

    Não escrevemos "funcions".
    Além disso,usamos o sinal "=>" para criá-la,o que lembra uma flecha,
    fazendo jus ao nome 'arrow Functions'.
*/ 

function MyGirlfriend(name) {                  // Funcions padrão
    return "MyGirlfriend: " + name 
    
}
const age = (age) => "MyGirlfriend tem: " + age  // arrow funcions -> () =>{}

console.log(MyGirlfriend("Daniele"))
console.log(age(39 + " age"))   

